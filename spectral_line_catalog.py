def wavelength_for(name):
    if name not in FULL_LINE_LIST:
        available = list(FULL_LINE_LIST.keys())
        suggestions = difflib.get_close_matches(name, available, n=5)
        hint = f" Closest matches: {', '.join(suggestions)}." if suggestions else ""
        raise KeyError(f"Unknown spectral line '{name}'.{hint}")
    return FULL_LINE_LIST[name]


def line_map(names):
    """Return OrderedDict preserving input order."""
    return OrderedDict((name, wavelength_for(name)) for name in names)


def wide_region_for(name):
    if name not in WIDE_REGION_LIST:
        available = list(WIDE_REGION_LIST.keys())
        suggestions = difflib.get_close_matches(name, available, n=5)
        hint = f" Closest matches: {', '.join(suggestions)}." if suggestions else ""
        raise KeyError(f"Unknown wide spectral region '{name}'.{hint}")
    return WIDE_REGION_LIST[name]


def wide_region_map(names):
    """Return OrderedDict preserving input order for wide regions."""
    return OrderedDict((name, wide_region_for(name)) for name in names)
